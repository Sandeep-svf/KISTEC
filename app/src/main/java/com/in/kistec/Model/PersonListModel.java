package com.in.kistec.Model;

public class PersonListModel {
    private String personNameList, usernameRequestedBy, status;


    public String getPersonNameList() {
        return personNameList;
    }

    public void setPersonNameList(String personNameList) {
        this.personNameList = personNameList;
    }

    public String getUsernameRequestedBy() {
        return usernameRequestedBy;
    }

    public void setUsernameRequestedBy(String usernameRequestedBy) {
        this.usernameRequestedBy = usernameRequestedBy;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
